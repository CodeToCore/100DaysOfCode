# hex() function
 
print("The hexadecimal form of 25 is "
                            + hex(25))
                             
print("The hexadecimal form of the "
      "ascii value is 'x' is " + hex(ord('x')))
       
print("The hexadecimal form of 12.26 is " + float.hex(12.26))

# OUTPUT

'''
The hexadecimal form of 25 is 0x19
The hexadecimal form of the ascii value is 'x' is 0x78
The hexadecimal form of 12.26 is 0x1.8851eb851eb85p+3
'''